@bp.route('/ask', methods=['POST'])
@login_required
def ask_question():
    """处理用户提问"""
    data = request.get_json()
    question = data.get('question', '').strip()
    
    if not question:
        return jsonify({'error': '请输入问题'}), 400
    
    # 在知识库中查找相关问题
    response = find_answer(question)
    
    return jsonify({
        'question': question,
        'answer': response['answer'],
        'category': response['category'],
        'similar_questions': response.get('similar', [])
    })

@bp.route('/quick-question/<category>/<int:index>')
@login_required
def quick_question(category, index):
    """快捷问题"""
    if category not in KNOWLEDGE_BASE:
        return jsonify({'error': '分类不存在'}), 404
    
    questions = KNOWLEDGE_BASE[category]
    if index >= len(questions):
        return jsonify({'error': '问题不存在'}), 404
    
    item = questions[index]
    return jsonify({
        'question': item['question'],
        'answer': item['answer'],
        'category': category,
        'similar_questions': [q['question'] for q in questions]
    })

@bp.route('/categories')
@login_required
def get_categories():
    """获取所有分类"""
    categories_data = []
    for category, questions in KNOWLEDGE_BASE.items():
        categories_data.append({
            'name': category,
            'count': len(questions),
            'sample': questions[0]['question'] if questions else ''
        })
    return jsonify(categories_data)

def find_answer(question):
    """根据问题查找答案"""
    question_lower = question.lower()
    
    # 遍历知识库查找最佳匹配
    best_match = None
    best_score = 0
    
    for category, questions in KNOWLEDGE_BASE.items():
        for item in questions:
            # 计算问题相似度
            q_words = set(re.findall(r'[\u4e00-\u9fa5]+', item['question'].lower()))
            u_words = set(re.findall(r'[\u4e00-\u9fa5]+', question_lower))
            
            # 关键词匹配得分
            score = 0
            for word in u_words:
                if word in item['question'].lower():
                    score += 2
                elif any(word in q for q in q_words):
                    score += 1
            
            # 检查关键词映射表
            for key, keywords in KEYWORDS_MAP.items():
                if any(kw in question_lower for kw in keywords):
                    if key == category or any(kw in item['question'].lower() for kw in keywords):
                        score += 3
            
            if score > best_score:
                best_score = score
                best_match = (category, item)
    
    if best_match and best_score > 0:
        category, item = best_match
        similar = [q['question'] for q in KNOWLEDGE_BASE[category] if q['question'] != item['question']][:3]
